let a = 5;
let b = 10;
let c = 8;

// Arithmetic operators
console.log(a,"+",b,"=",a+b);

// unary operator
c++;
console.log(c)    // output : 9

// Assignment operator
let d = 15;      // Assign
d += 10
console.log(d)   // output : 25

// Loose equlily
let num1 = 5;
let num2 = "5";
console.log(num1 == num2)   // output : true
// Strict equality
console.log(num1 === num2)  // output : false 
